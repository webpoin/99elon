			<dt><h2><strong>2</strong>炒金做单——三大雷区不要碰</h2></dt>
			<dd>
				<p>在掀起的黄金投资热潮中，有一个明显的变化，即黄金类投资更受青睐，尤其是高端客户，花数十万甚至上百万元购买实物黄金或是其他类型的炒金比比皆是。但炒金如何做单才能避免踏入投资雷区，投资黄金的心态又该如何调整呢?</p>
				<br>
				<h3>一忌：猜顶猜底，片面看重短期金价</h3>
				<br>
				<p>影响黄金的价格很多，在当前全球通货膨胀加剧的情况下，黄金的避险功能强化，导致黄金需求增加，金价上扬。由于最低点可遇而不可求，建议在黄金价格相对平稳或走低时再买进。此外，在进行黄金买卖时，不应片面看重短期金价，而忽略了金价是处于“大熊”还是“大牛”的趋势。但从历史经验来看，存在季节性黄金需求。由于每年的第三季度末是印度的婚嫁节，市场对于黄金的需求旺盛，因此每年的第三季度末和第四季度黄金价格通常都会走高。</p>
				<br>

				<h3>二忌：快进快出，盈利就平盘收钱</h3>
				<br>
				<p>作为非专业的普通投资者，想要通过快进快出的方法来炒黄金获利，可能会以失望告终。缺乏经验的投资者，在开盘买入或卖出某种货之后，一见有盈利，就立刻想到平盘收钱。获利平仓做起来似乎很容易，但是捕捉获利的时机却是一门学问。有经验的投资者，会根据自己对价格走势的判断，确定平仓的时间。如果认为市场形势会进一步朝着对他有利的方向发展，会耐着性子，明知有利而不赚，任由价格尽量向着自己更有利的方向发展，从而达到利润延续的最终目标。</p>
				<br>

				<h3>三忌：配置太多，过多看短期走势</h3>
				<br>
				<p>黄金属于中长线的投资工具，投资人要有长期投资收藏的心理准备，不要过多看短期走势，也不要存在侥幸心理。在投资过程中，当金价已上涨不少时，投资人对是否应大量购买必须谨慎。虽然黄金具有长期抵御风险的特征，但相对应的是其投资回报率也较低，因此黄金投资在个人投资组合中所占比例不宜太高。</p>

