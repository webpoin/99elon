		<dl>
			<dt><h2><strong>3</strong>到底是谁左右着金价？</h2></dt>
			<dd>
				<p>商场里我们经常都会看到柜台上放有标注“今日金价”的铭牌，而且每天的价格都各不相同。炒金也是，价格波动瞬息万变，行情只在一瞬之间，麻木投资你只会错失良机，炒金我们要了解影响价格的因素</p>
				<br>
				<h3>黄金需求与供给</h3>
				<p>（1）黄金生产量的增减：会影响到黄金供求平衡</p>
				<p>（2）季节性供求规律：一般会在二季度左右出现底部，到第三季度，受节日、降雨等因素推动，黄金消费需求会增强，尤其是在春节，受亚洲国家消费影响，需求增加，助推金价。</p>
				<p>（3）黄金生产成本：在1992年，因为黄金生产成本提高，不少的金矿停止生产，导致了金价的一度推高。</p>
				<p>（4）黄金在工业用途和装饰用途：电子业、牙医类、珠宝业等用金工业，在生产上出现的变动，都会影响到黄金的价格。</p>
				
				<br>
				<h3>政治因素</h3>
				<p>(1)政治局势：战争会使得物价上涨，令金价得到支撑。而世界和平则会使金价受到不利的影响。</p>
				<p>(2)政府行为：当政府需要套取外汇时，不管当时黄金的价格如何，都会沽出所储备的黄金来获得，政府黄金回收的数据，也是影响黄金价格的重要指标。</p>
				
				<br>
				<h3>美元走势</h3>
				<p>若美元走势强劲，投资美元收益会更大化，因此金价会下跌;而美元处于弱市时，投资者就会减少对美元的投资，转投金市，助推金价上涨。</p>
				
				<br>
				<h3>通货膨胀与利率</h3>
				<p>物价上涨导致通货膨胀，会刺激黄金的保值功能，金价也就有所波动。若利率提高，利空黄金，相反，利率下滑，有利金价上涨。</p>
				
				<br>
				<h3>相关市场</h3>
				<p>(1)、商品市场:国际商品市场尤其是有色金属价格的走势对金价有很重要的影响，因此分析和跟踪商品价格趋势就成为投资者必须面对和解决的问题。商品投资者通常以分析商品价格指数作为研判商品价格趋势的重要手段。</p>
				<p>(2)、原油:黄金具有抵御通货膨胀的功能，而国际原油价格与通胀水平密切相关，因此，黄金价格与国际原油价格具有正向互动关系。</p>
				<p>(3)、股市:在通常情况下，黄金与股市也是逆向运行的，股市行情大幅上扬时，黄金价格往往是下跌的，反之亦然。</p>
				
				<br>
				<h3>ETF持仓水平</h3>
				<p>近年来黄金价格的大幅上涨既是投资背景发生深刻变化的产物，也是国际对冲基金大肆炒作的结果，投资者在判断黄金价格走势时，必须密切关注国际基金持仓水平的动态变化。</p>

