				<p>从众心理，是指个人受到外界人群行为的影响，而在自己的知觉、判断、认识上表现出符合于公众舆论或多数人的行为方式。研究表明，从众心理是大部分个体普遍所有的心理现象。</p>

				<p>在黄金投资市场中，许多投资者也会受到这种“从众心理”的影响，市场低迷时，因为多数人都不看好市场低迷的状态，所以对于拥有绝佳投资机遇的时机都演变为了无人问津。等到市场真的走高，投资者才会争先恐后的入市。而市场一旦出现风吹草动，便又会蜂拥平仓离场，这样的例子不胜枚举。尤其是在经济过热、市场充满泡沫时表现得尤为明显，投资者莫名其妙地随波逐流、追涨杀跌，缺乏自己的判断。</p>

				<p><img src="images/topic_pic1.jpg" alt=""/></p>

				<h3>那么，在黄金投资中该如何去避免这种从众心理呢?</h3>
				<p>首先，我们要结合自身的投资目标、风险承受度等因素，合理设定获利点和止损点，同时学会控制情绪，在市场波动时保持平和心态，理性判断趋势，这样才能顺利达成投资目标。</p>

				<p>其次，设定获利点和止损点。学会在做单时设定获利点，可以提醒自己已达到投资目标，避免因一时贪心错失平仓良机，使获利缩水甚至变成亏损;止损点的设定则能够帮助我们控制投资风险，以避免可能产生的更大损失。一般而言，投资人可以结合自身的风险承受度、获利期望值、所处年龄阶段、家庭经济状况以及所在的市场特征加以考虑，同时定期检查投资回报情况，这样才能找出最适合自己投资组合的获利及止损区间。</p>

				<p>最后，合理鉴别市场行情。如果市场因为短期调整而触及止损点，此时不宜贸然赎回，投资者可根据自己的实际情况，考虑是否能够容忍继续持有的风险，如果市场长线看好，而投资者的风险承受能力大于市场回调，那么投资者可以重新设立警示条件，甚至趁机加码，以达到逢低摊平的目的。反之，如果市场回调超出投资者的承受能力，或者市场转向已成定局时，此时无论是否达到了投资者期望的获利点，都应该尽快平仓。</p>
